# 5. Contador de 1 a 10: Usando um laço while para imprimir os números de 1 a 10

# Inicializa o contador com o valor 1
contador = 1

# O laço vai continuar enquanto o contador for menor ou igual a 10
while contador <= 10:
    print(contador)  # Exibe o valor do contador
    contador += 1  # Aumenta o contador em 1 a cada iteração