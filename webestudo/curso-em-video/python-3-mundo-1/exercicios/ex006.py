# 6. Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.

numero = int(input('Digite um número: ')) # Recebe um numero do user

print(f'O dobro de {numero} é {numero * 2}.')
print(f'O tríplo de {numero} é {numero * 3}.')


print(f'E a raiz quadrada de {numero} é {numero ** 0.5:.2f}') # Tirar a raiz quadrada de um número é o mesmo que elevá-lo à potência 1/2. E 1/2 em forma decimal é 0.5.

